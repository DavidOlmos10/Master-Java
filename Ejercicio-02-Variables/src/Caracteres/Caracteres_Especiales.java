package Caracteres;

public class Caracteres_Especiales {

    public static void main(String args[]) {
        var nombre = "Sofia";

        System.out.println("Nueva linea: \n" + nombre); // salto de linea
        System.out.println("Tabulador \t" + nombre); // Espacio
        System.out.println("Retroceso: \b\b" + nombre); //pone a lado el nombre de la variable
        System.out.println("Comilla simple: \'" + nombre + " \'"); //Comilla simple
        System.out.println("Comilla doble: \" " + nombre + " \""); // Comilla donle
    }
}
